const mongoose = require("mongoose");

const chatSchema = new mongoose.Schema({
  from: {
    type: String,
    required: true,
  },
  to: {
    type: String,
    required: true,
  },
  message: {
    type: String,
    required: true,
  },
  time: {
    type: Date,
    default: Date.now,
  },
  read: {
    type: Boolean,
    default: false,
    required: true,
  },
});

module.exports = mongoose.model("Chat", chatSchema);
